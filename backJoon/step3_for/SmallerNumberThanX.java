package step3_for;
/*문제
정수 N개로 이루어진 수열 A와 정수 X가 주어진다. 이때, A에서 X보다 작은 수를 모두 출력하는 프로그램을 작성하시오.

입력
첫째 줄에 N과 X가 주어진다. (1 ≤ N, X ≤ 10,000)

둘째 줄에 수열 A를 이루는 정수 N개가 주어진다. 주어지는 정수는 모두 1보다 크거나 같고, 10,000보다 작거나 같은 정수이다.

출력
X보다 작은 수를 입력받은 순서대로 공백으로 구분해 출력한다. X보다 작은 수는 적어도 하나 존재한다.*/
import java.io.BufferedReader;
import java.io.BufferedWriter;
import java.io.IOException;
import java.io.InputStreamReader;
import java.io.OutputStreamWriter;
import java.util.StringTokenizer;

public class SmallerNumberThanX {
	public static void main(String[] args) throws IOException{
		BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
		BufferedWriter bw = new BufferedWriter(new OutputStreamWriter(System.out));
		String line1 = br.readLine();
		StringTokenizer st = new StringTokenizer(line1);
		int N = Integer.parseInt(st.nextToken());
		int A = Integer.parseInt(st.nextToken());

		String line2 = br.readLine(); // N에 맞게 자연수 를 입력해줌
		String array[] = line2.split(" "); // split 함수로 데이터만 뽑아 배열에 넣어준다.
		for(int i=0;i<N;i++){
			if(Integer.parseInt(array[i])<A){
			//bw.write(array[i]+" "); // 왜 안나올까..?
			System.out.print(array[i]+" "); // write 대체
			}
		}
	}
}
